        num = 0;
        boolean found = false;
        while(num<i && !found){
        if(name.equals(RI.potentials.get(num).getName()))
        {
        AddressText.setText(RI.potentials.get(num).getAddress());
        SexText.setText(RI.potentials.get(num).getSex());
        AgeText.setText(RI.potentials.get(num).getAge());
        found = true;
        }else{num= num+1;}
        
        }
